CREATE TABLE QUOTE  (
          ID BIGINT NOT NULL ,
          VERSION BIGINT,
          TEXT VARCHAR(65535 OCTETS) NOT NULL ,
          AUTHOR VARCHAR(255 OCTETS) NOT NULL ,
		  CREATION_DATE DATE NOT NULL ,
		  ACTIVE BOOLEAN NOT NULL,
		  CONSTRAINT quote_pk PRIMARY KEY (ID)
);

CREATE SEQUENCE quote_sequence START WITH 1 INCREMENT BY 50 MINVALUE 1 NO MAXVALUE NO CYCLE;